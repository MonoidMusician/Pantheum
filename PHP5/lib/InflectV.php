	static public function preterite($verb, $only_one=false) {
		foreach (self::$irregular as $i)
			if ($i[0] == $verb) {
				if ($only_one)
					return explode("|", $i[1])[0];
				return $i[1];
			}
		if (array_key_exists($verb, self::$presentparticiple)) {
			$v = self::$presentparticiple[$verb];
			$matches = [];
			preg_match('/([a-zA-Z-]+([a-zA-Z]))\2ing$/', $v, $matches);
			if (count($matches) and $matches[1] == $verb)
				return $verb.$matches[2]."ed";
		}
		if ($verb[strlen($verb)-1] == "e")
			return $verb."d";
		return $verb."ed";
	}

	static public function pastparticiple($verb, $only_one=false) {
		foreach (self::$irregular as $i)
			if ($i[0] == $verb) {
				if ($only_one)
					return explode("|", $i[2])[0];
				return $i[2];
			}
		if (array_key_exists($verb, self::$presentparticiple)) {
			$v = self::$presentparticiple[$verb];
			$matches = [];
			preg_match('/([a-zA-Z-]+([a-zA-Z]))\2ing$/', $v, $matches);
			if (count($matches) and $matches[1] == $verb)
				return $verb.$matches[2]."ed";
		}
		if ($verb[strlen($verb)-1] == "e")
			return $verb."d";
		return $verb."ed";
	}

	static public function thirdsingular($verb, $only_one=false) {
		if ($verb == "be") return "is";
		if ($verb == "have") return $only_one?"hath":"has|hath";
		$verb = preg_replace("/[^aeiou]y$/", "i", $verb);
		if (preg_match("/(i|o|ch|sh|ss|x)$/", $verb))
			return $verb."es";
		return $verb."s";
	}

	static public function secondsingular($verb, $only_one=false) {
		if ($verb == "be") return $only_one?"art":"art|are";
		if ($verb == "have") return $only_one?"hast":"hast|have|havest";
		if ($verb[strlen($verb)-1] == "e")
			return $verb."st";
		return $verb."est";
	}

	static public function presentparticiple($verb, $only_one=false) {
		if (array_key_exists($verb, self::$presentparticiple)) {
			if ($only_one)
				return explode("|", self::$presentparticiple[$verb])[0];
			return self::$presentparticiple[$verb];
		}
		if (endswith($verb,"ie")) return substr($verb,0,-2)."ying";
		if (endswith($verb,"e")) return substr($verb,0,-1)."ing";
		return $verb."ing";
	}
